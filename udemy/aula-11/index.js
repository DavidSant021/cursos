// Operadores Aritméticos: 
// + Adição e Concatenação
// - Subtração
// * Multiplicação
// / Divisão
// ** Potenciação
// % Resto da divisão

// Ordem de precedência: () | ** | / * % | + -


// Adição:
const num1 = 5;
const num2 = 10;
console.log(num1 + num2);

// Subtração:
console.log(num1 - num2);

// Multiplicação:
console.log(num1 * num2);

// Divisão:
console.log(num1 / num2);

// Potenciação:
console.log(num1 ** num2);

// Resto da Divisão:
console.log(num1 % num2);